            logger.warning(f"Webhook –Ω–µ –±—ã–ª —É–¥–∞–ª–µ–Ω! URL: {info.url}")
            # –ü–æ–≤—Ç–æ—Ä—è–µ–º –ø–æ–ø—ã—Ç–∫—É
            await bot_temp.delete_webhook(drop_pending_updates=True)
            info = await bot_temp.get_webhook_info()
            logger.info(f"–ü–æ–≤—Ç–æ—Ä–Ω–∞—è –ø–æ–ø—ã—Ç–∫–∞ —É–¥–∞–ª–µ–Ω–∏—è webhook: {info.url is None}")
        
